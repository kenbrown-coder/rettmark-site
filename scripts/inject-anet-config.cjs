/**
 * Writes js/anet-config.js for Accept.js (public values only).
 *
 * Netlify → Environment variables:
 *   ANET_PUBLIC_CLIENT_KEY   — Accept.js public client key
 *   ANET_API_LOGIN_ID        — API Login ID (required by Accept.js in the browser)
 *   ANET_SANDBOX             — "true" | "false" (default true when unset for safety)
 *
 * Run locally:
 *   PowerShell: $env:ANET_PUBLIC_CLIENT_KEY="..."; $env:ANET_API_LOGIN_ID="..."; node scripts/inject-anet-config.cjs
 */

var fs = require("fs");
var path = require("path");

var root = path.join(__dirname, "..");
var outPath = path.join(root, "js", "anet-config.js");

var clientKey = process.env.ANET_PUBLIC_CLIENT_KEY || process.env.AUTHORIZE_NET_CLIENT_KEY || "";
var apiLoginId = process.env.ANET_API_LOGIN_ID || process.env.AUTHORIZE_NET_API_LOGIN || "";
var sandboxRaw = (process.env.ANET_SANDBOX || "true").toLowerCase();
var sandbox = sandboxRaw !== "false" && sandboxRaw !== "0";

var banner =
  "/**\n" +
  " * Authorize.Net Accept.js public settings. Generated by scripts/inject-anet-config.cjs.\n" +
  " * Transaction keys never go here — only the serverless function uses those.\n" +
  " */\n";

var body =
  "window.RETTMARK_ANET = " +
  JSON.stringify(
    {
      clientKey: clientKey,
      apiLoginId: apiLoginId,
      sandbox: sandbox
    },
    null,
    0
  ) +
  ";\n";

fs.writeFileSync(outPath, banner + body, "utf8");

if (process.env.NETLIFY === "true" && (!clientKey || !apiLoginId)) {
  console.warn(
    "inject-anet-config: ANET_PUBLIC_CLIENT_KEY or ANET_API_LOGIN_ID missing; checkout Accept.js will be disabled until both are set."
  );
}
