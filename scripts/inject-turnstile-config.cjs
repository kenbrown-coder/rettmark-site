/**
 * Writes js/turnstile-config.js from environment (no secrets in git).
 * Netlify: Site configuration → Environment variables → add TURNSTILE_SITE_KEY
 * (or RETTMARK_TURNSTILE_SITE_KEY). Public site key only — never the Turnstile secret.
 *
 * Run locally:  set TURNSTILE_SITE_KEY=your_key&& node scripts/inject-turnstile-config.cjs
 * PowerShell:  $env:TURNSTILE_SITE_KEY="your_key"; node scripts/inject-turnstile-config.cjs
 */

var fs = require("fs");
var path = require("path");

var root = path.join(__dirname, "..");
var outPath = path.join(root, "js", "turnstile-config.js");

var key =
  process.env.TURNSTILE_SITE_KEY ||
  process.env.RETTMARK_TURNSTILE_SITE_KEY ||
  "";

var banner =
  "/**\n" +
  " * Cloudflare Turnstile site key (public). Generated by scripts/inject-turnstile-config.cjs\n" +
  " * from TURNSTILE_SITE_KEY (Netlify env). This file is overwritten on each Netlify build.\n" +
  " * Local dev: leave env unset for an empty key, or run the script with TURNSTILE_SITE_KEY set.\n" +
  " */\n";

var body = "window.RETTMARK_TURNSTILE_SITE_KEY = " + JSON.stringify(key) + ";\n";

fs.writeFileSync(outPath, banner + body, "utf8");

if (process.env.NETLIFY === "true" && !key) {
  console.warn(
    "inject-turnstile-config: TURNSTILE_SITE_KEY is not set; Turnstile will be disabled on the deployed site."
  );
}
